def get_quiz_prompt(genre: str, accuracy_rate: int = 0) -> str:
    genre = genre or "一般常識"

    return f"""プロのクイズ作家として、ジャンル「{genre}」の新しい早押しクイズを1問、以下のJSON形式のみで出力せよ。

【厳格なルール】
1. 難易度: 対象ジャンル愛好家が全文を読んだ際の正答率が約{accuracy_rate}%となるよう設定。
2. 正解: 表記揺れによらず一意に特定可能な1語の名詞。
3. 構成と自己完結性: 前半に難易度の高い事実(前フリ)、後半に一般的な事実を配置。事前知識に依存せず伝わるよう、唐突な界隈用語は避け「作品名/分野名/時代」等の前提を明示し、現実世界の視点で自己完結させる。
4. 確定ポイント: 途中で正解が確実に1つに絞り込める情報順序にする。
5. 日本語の自然さ(最重要): 単語や体言止めの不自然な羅列は避け、接続表現で滑らかな文にする。(悪例:「〜が研究、〜で示す それは何？」 良例:「〜が研究した、〜で表す定理は何？」)
6. 文体・字数: 30〜70文字。敬体を避け、句点(。)なしの1文で構成。
7. 語尾: 「〜は何/誰？」など、極力短い疑問形で終わる。
8. NG行為: 問題文中に正解の単語、その部分文字列、構成文字(漢字/かな等)を含めない。
9. 約物: 「」や読点などは自然な範囲で使用。

【出力フォーマット】
{{
    "question": "問題文",
    "answer": "正解"
}}
"""


def get_judge_prompt(expected_answer: str, user_answer: str) -> str:
    return f"""
以下の【想定正解】と【プレイヤーの解答】が、クイズの文脈において実質的に同じものを指しているか判定してください。
出力は、必ず true または false の文字列のみにしてください。

【判定基準】
・表記ゆれ（漢字・ひらがな・カタカナ、送り仮名の違い）、一般的な別名・略称は「正解 (true)」とする。
・解答に「〜です」「答えは〜」などの余分な語尾が含まれていても、本質的な解答部分が合っていれば「正解 (true)」とする。
・誤字脱字（タイポ）については、明らかに正解を意図していると判断できる軽微なもの（1文字の変換ミスなど）は「正解 (true)」とする。
・全く別の概念や、上位/下位すぎる概念（例: 想定正解「りんご」に対し、解答「果物」など）は「不正解 (false)」とする。

【想定正解】: {expected_answer}
【プレイヤーの解答】: {user_answer}
"""
