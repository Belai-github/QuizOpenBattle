def get_quiz_prompt(genre: str) -> str:
    if genre == "":
        genre = "一般常識"
    return f"""
    あなたはプロのクイズ作家です。
    ジャンル「{genre}」に関する新しい早押しクイズを1問生成し、以下のJSONフォーマットのみで出力してください。
    マークダウンや余計な解説は一切含めないでください。

    【要件】
    1. 答えは必ず「誰もが知っている名詞」にすること。
    2. 問題文の前半はやや難易度の高い事実（前フリ）を配置し、後半になるにつれて一般的な事実を配置すること。
    3. 表記揺れなどを無視して正解が一意に定まること。
    4. 問題文は30文字以上60文字以下に収め、最後は必ず「〜は何でしょう？」「〜は誰でしょう？」などの疑問形で終わること。
    5. 問題文の中に、正解の単語（またはその一部の漢字・ひらがな・カタカナ）を絶対に含めないこと。
    6. 「」（かぎかっこ）や、（読点）などの約物は、日本語として不自然にならない範囲で積極的に使用すること。

    【出力フォーマット】
    {{
        "question": "問題文をここに",
        "answer": "想定される正解（短い名詞）をここに"
    }}
    """


def get_judge_prompt(expected_answer: str, user_answer: str) -> str:
    return f"""
    以下の【想定正解】と【プレイヤーの解答】が、クイズの文脈において実質的に同じものを指しているか判定してください。
    出力は、必ず true または false の文字列のみにしてください。

    【判定基準】
    ・表記ゆれ（漢字・ひらがな・カタカナ、送り仮名の違い）、一般的な別名・略称は「正解 (true)」とする。
    ・解答に「〜です」「答えは〜」などの余分な語尾が含まれていても、本質的な解答部分が合っていれば「正解 (true)」とする。
    ・誤字脱字（タイポ）については、明らかに正解を意図していると判断できる軽微なもの（1文字の変換ミスなど）は「正解 (true)」とする。
    ・全く別の概念や、上位/下位すぎる概念（例: 想定正解「りんご」に対し、解答「果物」など）は「不正解 (false)」とする。

    【想定正解】: {expected_answer}
    【プレイヤーの解答】: {user_answer}
    """
