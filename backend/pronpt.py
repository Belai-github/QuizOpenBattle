def get_quiz_prompt(genre: str, accuracy_rate: int = 0) -> str:
    if not genre:
        genre = "一般常識"

    return f"""
あなたはプロのクイズ作家です。
ジャンル「{genre}」に関する新しい早押しクイズを1問生成し、以下のJSONフォーマットのみで出力してください。

【問題作成の厳格なルール】
1. 難易度: このジャンルに一定以上の興味関心がある成人が全文を読んだ場合、正答率がおよそ {accuracy_rate}% となるような問題を作成すること。
2. 正解: 表記揺れを無視して一意に正解が特定できるような問題を作成すること。正解は必ず1語の名詞になること。
3. 構成と自己完結性: 前半にやや難易度の高い事実（前フリ）を置き、後半になるにつれて一般的な事実を配置すること。
4. 自己完結性: 解答者が事前にジャンルを知らなくても何の話題か分かるよう、特定の界隈内でのみ通用する設定を唐突に出さず、必ず「作品名」「分野名」「時代」などの前提があるならそれを明示して、現実世界の視点で自己完結した問題文にすること。
5. 確定ポイント: 問題文の途中で正解が確実に1つに絞り込めるよう、情報提示の順番を工夫すること。
6. 日本語の自然さ（最重要）: 単語や体言止めの不自然な羅列は絶対に避け、「〜であり」「〜した」などの接続表現を用いて、人間が読み上げるのに適した滑らかで自然な日本語の文章にすること。

【悪い出力例（不自然な日本語）】
"古代ギリシャの学派が研究、直角三角形の3辺の長さをa^2+b^2=c^2で示す それは何？"
※理由: 接続詞がなく、単語の羅列になっており不自然。

【良い出力例（自然な日本語）】
"古代ギリシャのピタゴラス学派が研究した、直角三角形の3辺の長さの関係を「a^2+b^2=c^2」で表す定理は何？"

7. 文体と文字数: 全体で30文字以上70文字以下とし、句点（。）を使わず1つの文で構成すること。敬体（です・ます）は使用しないこと。
8. 語尾: 最後は必ず「〜は何？」「〜は誰？」など、極力短い疑問形で終わること。
9. NG行為: 問題文の中に、正解の単語またはその部分文字列、およびその一部の漢字・ひらがな・カタカナを正解を直接指す文脈で絶対に含めないこと。
10. 約物の使用: 「」（かぎかっこ）や、（読点）などの約物は、日本語として不自然にならない範囲で使用すること。

【出力フォーマット】
{{
"question": "問題文をここに",
"answer": "想定される正解（短い名詞）をここに"
}}
"""


def get_judge_prompt(expected_answer: str, user_answer: str) -> str:
    return f"""
以下の【想定正解】と【プレイヤーの解答】が、クイズの文脈において実質的に同じものを指しているか判定してください。
出力は、必ず true または false の文字列のみにしてください。

【判定基準】
・表記ゆれ（漢字・ひらがな・カタカナ、送り仮名の違い）、一般的な別名・略称は「正解 (true)」とする。
・解答に「〜です」「答えは〜」などの余分な語尾が含まれていても、本質的な解答部分が合っていれば「正解 (true)」とする。
・誤字脱字（タイポ）については、明らかに正解を意図していると判断できる軽微なもの（1文字の変換ミスなど）は「正解 (true)」とする。
・全く別の概念や、上位/下位すぎる概念（例: 想定正解「りんご」に対し、解答「果物」など）は「不正解 (false)」とする。

【想定正解】: {expected_answer}
【プレイヤーの解答】: {user_answer}
"""
